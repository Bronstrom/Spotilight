    <hr />

    <p>
      You can view information on how to use the application and how to use it
      on this page.
    </p>

    <h2>Logging in/out of your account</h2>
    <hr />

    <p>
      In the top application navigation bar, you have the ability to login or
      logout of your account by clicking on the circular profile icon. You are
      signed in if your own Spotify profile icon appears in the circle;
      otherwise, a placeholder user icon will appear within the circle
      indicating no user is logged in. Once clicking on the circular icon menu
      options will dropdown. If you are logged in, you will have the "Logout"
      option, if you are not logged in, you will have the "Login" option. When
      clicking on "Login", you go through a similar flow to
      <a href="#authentification">Authentification</a>. When clicking on
      "Logout", you will be directly logged out of Spotify and met with a screen
      saying "You are logged out."
    </p>
  </div>
